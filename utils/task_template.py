from pathlib import Path
from datetime import datetime
from typing import Optional


# ============================================================================
# BASE TEMPLATE (Used by ALL watchers)
# ============================================================================

BASE_METADATA_TEMPLATE = """---
type: {type}
task_id: {task_id}
original_name: {original_name}
{additional_yaml_fields}detected: {detected}
priority: {priority}
status: pending

# AI Processing (filled by Claude Runner)
ai_processed: [PENDING]
ai_processed_at: [PENDING]
ai_category: [PENDING]
ai_decision: [PENDING]
ai_action_taken: [PENDING]
ai_response: |
  [PENDING]
---

# {title}: {original_name}

{header_fields}

---

## Content

```
{content}
```

---

## Suggested Actions

{actions}

---

## Processing Notes

<!-- CLAUDE_RESPONSE_START -->
**Category:** [PENDING]
**Decision:** [PENDING]
**Action Taken:** [PENDING]

**AI Response:**
[PENDING]
<!-- CLAUDE_RESPONSE_END -->

---

*Generated by AI Employee {watcher_name}*
*Task ID: `{task_id}`*
"""


