from pathlib import Path
from datetime import datetime
from typing import Tuple


# ============================================================================
# INTERNAL UTILITIES (minimal)
# ============================================================================

def _format_size(size: int) -> str:
    """Format file size as human-readable string."""
    for unit in ["B", "KB", "MB", "GB"]:
        if size < 1024:
            return f"{size:.1f} {unit}"
        size /= 1024
    return f"{size:.1f} TB"


def _make_safe_stem(name: str, max_len: int = 40) -> str:
    """Convert name into safe task ID stem."""
    stem = Path(name).stem
    safe = "".join(c if c.isalnum() or c in "._-" else "_" for c in stem)
    return safe[:max_len]


def _truncate_content(content: str, max_length: int = 4000) -> str:
    """Truncate content (Claude context is large but we stay safe)."""
    if len(content) <= max_length:
        return content
    truncated = content[: max_length - 250]
    omitted = len(content) - max_length + 250
    return (
        truncated
        + f"\n\n---\n\n[Content truncated — {omitted} characters omitted. "
        f"Full original is available in Gmail.]\n"
    )


